package Backend;

/**
 * CurrencyType — типы валют, используемые в системе магазина и баланса.
 *
 * <p>📌 Определяет, в какой валюте выполняются операции:
 * <ul>
 *   <li>покупка предметов</li>
 *   <li>пополнение баланса</li>
 *   <li>списание средств</li>
 * </ul>
 *
 * <p>📌 Используется в:
 * <ul>
 *   <li>BalanceManager</li>
 *   <li>ShopService</li>
 * </ul>
 *
 * <p>Фронтенд использует этот enum косвенно через API магазина и баланса.</p>
 */
public enum CurrencyType {

    /**
     * COINS — внутриигровая валюта.
     *
     * <p>📌 Используется для:
     * <ul>
     *   <li>покупки скинов</li>
     *   <li>покупки фонов</li>
     *   <li>покупки внутриигровых предметов</li>
     * </ul>
     */
    COINS,

    /**
     * REAL_MONEY — реальная валюта пользователя.
     *
     * <p>📌 Используется для:
     * <ul>
     *   <li>покупки внутриигровых монет</li>
     *   <li>донат-операций</li>
     * </ul>
     */
    REAL_MONEY
}
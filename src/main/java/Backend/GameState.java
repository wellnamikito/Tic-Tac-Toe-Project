package Backend;

import java.util.*;

/**
 * GameState — интерфейс состояния игры.
 *
 * <p>📌 Используется как основа для реализации логики игры,
 * включая ИИ (Minimax) и обработку ходов.</p>
 *
 * <p>Фронтенд напрямую не работает с этим интерфейсом,
 * но его реализация определяет поведение игры на сервере.</p>
 *
 * <p>📌 Основные задачи:
 * <ul>
 *   <li>проверка завершения игры</li>
 *   <li>оценка текущего состояния</li>
 *   <li>генерация возможных ходов</li>
 *   <li>определение текущего игрока</li>
 * </ul>
 */
public interface GameState {

    /**
     * Проверяет, завершена ли игра.
     *
     * @return true — если игра окончена (победа/ничья), иначе false
     */
    boolean isGameOver();

    /**
     * Оценивает текущее состояние игры.
     *
     * <p>📌 Используется ИИ (Minimax) для выбора лучшего хода.</p>
     *
     * <p>Принятая шкала:
     * <ul>
     *   <li>+10 — победа текущего игрока</li>
     *   <li>-10 — проигрыш</li>
     *   <li>0 — ничья или нейтральное состояние</li>
     * </ul>
     *
     * @return числовая оценка позиции
     */
    int evaluateScore();

    /**
     * Возвращает список всех возможных следующих состояний игры.
     *
     * <p>📌 Используется для построения дерева решений (Minimax).</p>
     *
     * @return список возможных ходов
     */
    List<GameState> getPossibleMoves();

    /**
     * Определяет, чей сейчас ход.
     *
     * <p>📌 Используется для разделения:
     * <ul>
     *   <li>Max игрока (AI / выигрывающая сторона)</li>
     *   <li>Min игрока (противник)</li>
     * </ul>
     *
     * @return true — если ход максимизирующего игрока
     */
    boolean isMaxPlayerTurn();
}
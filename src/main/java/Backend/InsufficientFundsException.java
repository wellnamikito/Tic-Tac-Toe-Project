package Backend;

/**
 * InsufficientFundsException — исключение недостатка средств у пользователя.
 *
 * <p>📌 Выбрасывается, когда пользователь пытается:
 * <ul>
 *   <li>потратить больше монет, чем у него есть</li>
 *   <li>купить предмет без достаточного баланса</li>
 *   <li>выполнить операцию списания средств при недостатке валюты</li>
 * </ul>
 *
 * <p>📌 Используется в:
 * <ul>
 *   <li>ShopService (покупки)</li>
 *   <li>BalanceManager (списание средств)</li>
 * </ul>
 *
 * <p>⚠️ Важно для фронтенда:
 * это исключение НЕ является критической ошибкой сервера.
 * Его нужно обрабатывать на клиенте и показывать пользователю понятное сообщение
 * (например: "Недостаточно средств").</p>
 */
public class InsufficientFundsException extends Exception {

    /**
     * Создаёт исключение с текстовым сообщением.
     *
     * @param message описание причины ошибки (например "Not enough coins")
     */
    public InsufficientFundsException(String message) {
        super(message);
    }
}